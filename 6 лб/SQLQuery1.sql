SELECT FACULTY.FACULTY, GROUPS.PROFESSION, GROUPS.YEAR_FIRST,
round(avg(cast(PROGRESS.NOTE as float(4))),2) as Средняя_оценка
FROM FACULTY inner join GROUPS
ON FACULTY.FACULTY = GROUPS.FACULTY
inner join STUDENT
ON STUDENT.IDGROUP = GROUPS.IDGROUP
inner join PROGRESS
ON PROGRESS.IDSTUDENT = STUDENT.IDSTUDENT
WHERE PROGRESS.SUBJECT IN ('СУБД','ОАиП')
GROUP BY FACULTY.FACULTY, GROUPS.PROFESSION, GROUPS.YEAR_FIRST
ORDER BY Средняя_оценка desc
