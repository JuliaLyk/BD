SELECT *
FROM(SELECT CASE 
WHEN NOTE between 8 and 9 then '8-9'
WHEN NOTE between 6 and 7 then '6-7'
WHEN NOTE between 4 and 5 then '4-5'
else 'меньше 4'
end [Оценка],count(*)[количество]
FROM PROGRESS group by case
WHEN NOTE between 8 and 9 then '8-9'
WHEN NOTE between 6 and 7 then '6-7'
WHEN NOTE between 4 and 5 then '4-5'
else 'меньше 4'
end) as T
order by Case[Оценка]
when '4-5' then 3
when '6-7' then 2
when '8-9' then 1
when 'меньше 4' then 4
else 0
end
